public class notas{
   private int mat;
   private int comp;
   private int fis;
   int x= mat;
   int y= comp;
   int z= fis;
   public notas(int x, int y, int z){
       this.x = x;
       this.y = y;
       this.z = z;
    }
   public int getx(){
       return x;
    }
   public int gety(){
       return y;
    }
   public int getz(){
       return z;
    } 
}
